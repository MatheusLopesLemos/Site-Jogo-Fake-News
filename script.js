

var contador = 0;
var tentativas = 0;
var numeroPerguntas = 19;

function Placar() {
  if (tentativas == numeroPerguntas){
    if (contador == 1) {
      
      setTimeout(() => {  window.alert("Você acertou " + contador + " resposta"); }, 200);
      
    } else {
      setTimeout(() => {  window.alert("Você acertou " + contador + " respostas"); }, 200);
    }
  }
}


function MudarEstado1(respostaCerta) {
  if (respostaCerta) {
    contador++;
  }
  tentativas++;
  
  document.getElementById("1").style.display = 'none';
  document.getElementById("2").style.display = 'block';
  Placar()
}

